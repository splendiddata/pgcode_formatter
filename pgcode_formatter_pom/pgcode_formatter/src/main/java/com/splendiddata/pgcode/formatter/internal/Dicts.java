    /**
     * Contains all keywords that are used in PlPGSQL code. This set contains all PostgreSQL keywords from
     * {@link #pgKeywords} and all specific PlPGSQL keywords in upper case.
     */
    public static final Set<String> pgPlPgsqlKeywords;

    /**
     * No instances
     * 
     * @throws UnsupportedOperationException
     *             in all cases
     */
    private Dicts() {
        throw new UnsupportedOperationException("No instances for class " + getClass().getName());
    }

    static {
        /*
         * Load all plpgsql keywords and combine them with the standard PostgreSQL keywords
         */
        Set<String> keywordSet = new HashSet<>(pgKeywords);
        try (BufferedReader reader = new BufferedReader(
                new InputStreamReader(Dicts.class.getResourceAsStream("plpgsql_keywords")))) {
            for (String line = reader.readLine(); line != null; line = reader.readLine()) {
                for (String word : line.split("\\W+")) {
                    if (!word.equals("")) {
                        keywordSet.add(word.toUpperCase());
                    }
                }
            }
        } catch (IOException e) {
            log.error("Failed to load " + PLPGSQL_KEYWORDS_PATH, e);
        }
        pgPlPgsqlKeywords = Collections.unmodifiableSet(keywordSet);
        log.debug(() -> "pgPlPgsqlKeywords = " + pgPlPgsqlKeywords.stream().sorted().collect(Collectors.joining(", ")));
    }
